class Case:

    def __init__(self, posX, posY):
        self.posX = posX
        self.posY = posY

    def getPosX(self):
        return self.posX
    def getPosY(self):
        return self.posY

    def setPosX(self, posX):
        self.posX = posX
    def setPosY(self, posY):
        self.posY = posY
